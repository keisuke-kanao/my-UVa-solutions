int main()
{
	const int exponent_max = 63;
	while (true) {
		unsigned long long n;
		scanf("%llu", &n);
		if (!n)
			break;
		n--;
		putchar('{');
		bool printed = false;
		unsigned long long b = 1;
		for (int i = 0; i <= exponent_max; i++, b <<= 1)
			if (n & b) {
				if (printed)
					putchar(',');
				printed = true;
				printf(" %s", power_of_threes[i]);
			}
		printf(" }\n");
	}
	return 0;
}

